export type MenuIconName = keyof typeof iconComponents;

export const menuIconItems = Object.keys(iconComponents).map(value => ({
  label: value
    .replace(/Outlined$/, '')
    .replace(/([a-z])([A-Z])/g, '$1 $2'),
  value,
}));

/**
 * 根据菜单资源保存的图标名称创建 Ant Design 图标节点。
 *
 * @param iconName 图标组件名称
 * @returns 匹配到的图标节点
 */
export function getMenuIcon(iconName?: string | null) {
  if (!iconName) {
    return undefined;
  }
  const IconComponent = iconComponents[iconName as MenuIconName];
  return IconComponent ? <IconComponent /> : undefined;
}
